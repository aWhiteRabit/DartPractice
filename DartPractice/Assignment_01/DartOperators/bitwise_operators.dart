void main(){
  var x = 20, y = 5;
  print("x & y = ${x & y}");
  print("x | y = ${x | y}");
  print("x ^ y = ${x ^ y}");
  print("~x = ${(~x)}");
  print("x << 2 = ${x << 2}");
  print("y >> 2 = ${y >> 2}");
}
